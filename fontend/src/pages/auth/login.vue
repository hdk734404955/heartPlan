import { useAuthStore } from '@/store/modules/auth'
import { useUserStore } from '@/store/modules/user'
import { authAPI } from '@/api/auth'

export default {
  name: 'LoginPage',
  setup() {
    // Store
    const authStore = useAuthStore()
    const userStore = useUserStore()
    
    // ÂìçÂ∫îÂºèÊï∞ÊçÆ
    const loginFormRef = ref(null)
    const showPassword = ref(false)
    
    // Ë°®ÂçïÊï∞ÊçÆ
    const loginForm = reactive({
      email: '',
      password: ''
    })
    
    // Ë°®ÂçïÈ™åËØÅËßÑÂàô
    const loginRules = reactive({
      email: [
        {
          required: true,
          message: 'Email is required',
          trigger: ['blur', 'change']
        },
        {
          type: 'email',
          message: 'Please enter a valid email address',
          trigger: ['blur', 'change']
        }
      ],
      password: [
        {
          required: true,
          message: 'Password is required',
          trigger: ['blur', 'change']
        },
        {
          min: 6,
          message: 'Password must be at least 6 characters',
          trigger: ['blur', 'change']
        }
      ]
    })
    

    
    // ÊñπÊ≥ï
    const togglePassword = () => {
      showPassword.value = !showPassword.value
    }
    
    const validateEmail = () => {
      if (loginFormRef.value) {
        loginFormRef.value.validateField('email')
      }
    }
    
    const handleLogin = async () => {
      try {
        // Ë°®ÂçïÈ™åËØÅ
        const valid = await loginFormRef.value.validate()
        if (!valid) return
        
        // ËÆæÁΩÆÂä†ËΩΩÁä∂ÊÄÅ
        authStore.setLoginLoading(true)
        
        // Ë∞ÉÁî®ÁôªÂΩïAPI - ÂìçÂ∫îÊã¶Êà™Âô®Â∑≤Â§ÑÁêÜÁªü‰∏ÄÊ†ºÂºèÔºåÁõ¥Êé•Ëé∑Âèñdata
        const responseData = await authAPI.login(loginForm)
        
        // ‰øùÂ≠òËÆ§ËØÅ‰ø°ÊÅØ
        authStore.setTokens({
          accessToken: responseData.accessToken,
          refreshToken: responseData.refreshToken
        })
        
        // ‰øùÂ≠òÁî®Êà∑‰ø°ÊÅØ
        userStore.setUserProfile(responseData.user)
        
        // ÊòæÁ§∫ÊàêÂäüÊèêÁ§∫
        uni.showToast({
          title: 'Login Successful',
          icon: 'success',
          duration: 2000
        })
        
        // Ë∑≥ËΩ¨Âà∞‰∏ªÈ°µÈù¢
        uni.reLaunch({
          url: '/pages/main/index'
        })
      } catch (error) {
        console.error('Login error:', error)
        
        // ÊòæÁ§∫ÈîôËØØÊèêÁ§∫ - ÂìçÂ∫îÊã¶Êà™Âô®Â∑≤Â§ÑÁêÜÈîôËØØ‰ø°ÊÅØÊòæÁ§∫
        // ËøôÈáåÂè™ÈúÄË¶ÅËÆ∞ÂΩïÊó•ÂøóÔºåÁî®Êà∑Â∑≤ÁªèÁúãÂà∞ÈîôËØØÊèêÁ§∫
      } finally {
        // Ê∏ÖÈô§Âä†ËΩΩÁä∂ÊÄÅ
        authStore.setLoginLoading(false)
      }
    }
    
    const handleForgotPassword = () => {
      uni.showModal({
        title: 'Forgot Password',
        content: 'Password reset feature will be available soon.',
        showCancel: false,
        confirmText: 'OK'
      })
    }
    
    const goToRegister = () => {
      uni.navigateTo({
        url: '/pages/auth/register'
      })
    }
    
    // ÁîüÂëΩÂë®Êúü
    onMounted(() => {
      // ÈöêËóèÂ∫ïÈÉ®tabÔºàÂ¶ÇÊûúÊòØtabBarÈ°µÈù¢Ôºâ
      try {
        uni.hideTabBar()
      } catch (error) {
        // ÈùûtabBarÈ°µÈù¢‰ºöÊä•ÈîôÔºåÂøΩÁï•Âç≥ÂèØ
      }
    })
    
    return {
      // Store
      authStore,
      
      // ÂìçÂ∫îÂºèÊï∞ÊçÆ
      loginFormRef,
      showPassword,

      loginForm,
      loginRules,
      

      
      // ÊñπÊ≥ï
      togglePassword,
      validateEmail,
      handleLogin,
      handleForgotPassword,
      goToRegister,
    }
  }
}
</script>

